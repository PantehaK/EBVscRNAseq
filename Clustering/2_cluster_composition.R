    mutate(new_cluster = factor(as.character(.data[[cfg$activated_cluster_col]]), levels = cluster_order)) %>%
    count(stim_status, new_cluster, name = "n") %>%
    group_by(stim_status) %>%
    mutate(prop = n / sum(n)) %>%
    ungroup()
  
  p_bar <- ggplot(plot_df, aes(x = stim_status, y = prop, fill = new_cluster)) +
    geom_col(width = 0.65, colour = "white", linewidth = 0.25) +
