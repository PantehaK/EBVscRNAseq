run_baseline_ebv_vs_negative <- function(obj, cfg) {
  message("\nRunning baseline EBV vs Negative composition analysis")
  
  require_metadata_columns(
    obj,
    c(
      cfg$sample_col,
      cfg$cohort_col,
      cfg$tetramer_col,
      cfg$virus_col,
      cfg$baseline_celltype_col
    ),
    object_name = "baseline object"
  )
  
  outdir <- cfg$baseline_output_dir
  create_dir(outdir)
  
  meta_clean <- obj@meta.data %>%
    as.data.frame() %>%
    rownames_to_column("cell") %>%
    mutate(
      patient_id = as.character(.data[[cfg$sample_col]]),
      cohort = clean_cohort(.data[[cfg$cohort_col]], ms_label = "MS"),
      cohort = factor(cohort, levels = c("Control", "MS")),
      tetramer_clean = str_to_upper(str_trim(as.character(.data[[cfg$tetramer_col]]))),
      virus = clean_virus(.data[[cfg$virus_col]]),
      virus = factor(virus, levels = c("Negative", "EBV")),
      celltype_new = factor(.data[[cfg$baseline_celltype_col]], levels = celltype_order)
    ) %>%
    filter(
      !str_detect(coalesce(tetramer_clean, ""), cfg$exclude_tetramer_regex),
      virus %in% c("Negative", "EBV"),
      !is.na(celltype_new),
      !is.na(patient_id)
    )
  
  rpqg_check <- meta_clean %>%
    summarise(
      remaining_excluded_tetramer_cells = sum(
        str_detect(coalesce(tetramer_clean, ""), cfg$exclude_tetramer_regex)
      ),
      total_cells_after_exclusion = n()
    )
  
  write_csv(
    rpqg_check,
    file.path(outdir, "baseline_EBV_vs_negative_RPQG_exclusion_check.csv")
  )
  
  plot_df <- meta_clean %>%
    count(virus, celltype_new, name = "n") %>%
    group_by(virus) %>%
    mutate(prop = n / sum(n)) %>%
    ungroup()
  
  p_bar <- ggplot(plot_df, aes(x = virus, y = prop, fill = celltype_new)) +
    geom_col(width = 0.65, colour = "white", linewidth = 0.25) +
    scale_fill_manual(values = celltype_cols, drop = FALSE) +
    scale_x_discrete(labels = c("Negative" = "Non-EBV", "EBV" = "EBV")) +
    scale_y_continuous(labels = percent_format(accuracy = 1), expand = c(0, 0)) +
    labs(
      x = NULL,
      y = "Proportion of cells",
      fill = "Cell type"
    ) +
    theme_classic(base_size = 14) +
    theme(
      axis.text.x = element_text(size = 13, colour = "black"),
      axis.text.y = element_text(size = 12, colour = "black"),
      axis.title.y = element_text(size = 14, colour = "black"),
      legend.title = element_text(size = 12),
      legend.text = element_text(size = 11)
    )
  
  save_plot_with_pdf(
    p_bar,
    file.path(outdir, "stacked_barplot_EBV_vs_negative_excluding_RPQG"),
    width = 8,
    height = 6,
    dpi = cfg$png_dpi,
    bg = "transparent",
    save_pdf = cfg$save_pdf
  )
  
  write_csv(
    plot_df,
    file.path(outdir, "stacked_barplot_EBV_vs_negative_excluding_RPQG.csv")
  )
  
  patient_prop_df <- meta_clean %>%
    filter(
      cohort %in% c("Control", "MS"),
      !is.na(patient_id)
    ) %>%
    count(patient_id, cohort, virus, celltype_new, name = "n") %>%
    complete(
      nesting(patient_id, cohort),
      virus = factor(c("Negative", "EBV"), levels = c("Negative", "EBV")),
      celltype_new = factor(celltype_order, levels = celltype_order),
      fill = list(n = 0)
    ) %>%
    group_by(patient_id, cohort, virus) %>%
    mutate(
      total_cells = sum(n),
      prop = if_else(total_cells > 0, n / total_cells, NA_real_)
    ) %>%
    ungroup()
  
  prop_wide <- patient_prop_df %>%
    select(patient_id, cohort, virus, celltype_new, prop, total_cells) %>%
    pivot_wider(
      names_from = virus,
      values_from = c(prop, total_cells),
      names_sep = "_"
    ) %>%
    filter(
      total_cells_Negative > 0,
      total_cells_EBV > 0
    )
  
  stats_df <- prop_wide %>%
    group_by(cohort, celltype_new) %>%
    summarise(
      n_patients = sum(!is.na(prop_Negative) & !is.na(prop_EBV)),
      median_negative = median(prop_Negative, na.rm = TRUE),
      median_ebv = median(prop_EBV, na.rm = TRUE),
      median_difference = median(prop_EBV - prop_Negative, na.rm = TRUE),
      p_value = run_paired_wilcox(prop_EBV, prop_Negative, min_n = cfg$min_paired_samples),
      .groups = "drop"
    ) %>%
    group_by(celltype_new) %>%
    mutate(FDR = p.adjust(p_value, method = "BH")) %>%
    ungroup() %>%
    mutate(
      p_label = paste0("FDR=", format_p(FDR)),
      significance = case_when(
        is.na(FDR) ~ "NA",
        FDR < 0.001 ~ "***",
        FDR < 0.01 ~ "**",
        FDR < 0.05 ~ "*",
        TRUE ~ "ns"
      ),
      celltype_new = factor(celltype_new, levels = celltype_order)
    ) %>%
    arrange(cohort, celltype_new)
  
  write_csv(
    stats_df,
    file.path(outdir, "EBV_vs_negative_celltype_proportion_stats_paired_wilcox_by_cohort_excluding_RPQG.csv")
  )
  
  write_csv(
    prop_wide,
    file.path(outdir, "EBV_vs_negative_celltype_proportion_sample_by_cohort_excluding_RPQG.csv")
  )
  
  scatter_df <- patient_prop_df %>%
    filter(!is.na(prop)) %>%
    semi_join(
      prop_wide %>% distinct(patient_id, cohort),
      by = c("patient_id", "cohort")
    ) %>%
    mutate(
      celltype_new = factor(celltype_new, levels = celltype_order),
      cluster_col = unname(celltype_cols[as.character(celltype_new)]),
      point_fill = if_else(virus == "EBV", cluster_col, "white"),
      point_outline = cluster_col,
      virus = factor(virus, levels = c("Negative", "EBV"))
    )
  
  plot_one_celltype <- function(ct) {
    df_ct <- scatter_df %>% filter(celltype_new == ct)
    
    stat_ct <- stats_df %>%
      filter(celltype_new == ct) %>%
      mutate(
        star_label = if_else(significance == "ns", "", significance),
        y_pos = 0.98
      )
    
    celltype_colour <- unname(celltype_cols[as.character(ct)])
    
    ggplot(df_ct, aes(x = virus, y = prop, group = patient_id)) +
      geom_line(colour = scales::alpha(celltype_colour, 0.35), linewidth = 0.5) +
      geom_point(
        aes(fill = point_fill, colour = point_outline),
        shape = 21,
        size = 3.2,
        stroke = 0.9,
        alpha = 1
      ) +
      geom_text(
        data = stat_ct,
        aes(x = 1.5, y = y_pos, label = star_label),
        inherit.aes = FALSE,
        size = 12,
        fontface = "bold",
        vjust = 1
      ) +
      facet_wrap(~ cohort, nrow = 1) +
      scale_fill_identity() +
      scale_colour_identity() +
      scale_x_discrete(labels = c("Negative" = "Non-EBV", "EBV" = "EBV")) +
      scale_y_continuous(
        limits = c(0, 1),
        breaks = seq(0, 1, by = 0.2),
        labels = percent_format(accuracy = 1),
        expand = expansion(mult = c(0, 0.02))
      ) +
      labs(
        title = as.character(ct),
        x = NULL,
        y = "Proportion of cells"
      ) +
      theme_classic(base_size = 14) +
      theme(
        plot.title = element_text(face = "bold", size = 14, hjust = 0.5),
        strip.text = element_blank(),
        strip.background = element_blank(),
        axis.text.x = element_text(size = 14, colour = "black"),
        axis.text.y = element_text(size = 14, colour = "black"),
        axis.title.y = element_text(size = 15, colour = "black"),
        legend.position = "none"
      )
  }
  
  pdf(
    file.path(outdir, "paired_scatter_EBV_vs_negative_celltype_proportions_by_cohort_excluding_RPQG_one_page_per_celltype.pdf"),
    width = 7,
    height = 5
  )
  
  for (ct in celltype_order) {
    if (any(scatter_df$celltype_new == ct, na.rm = TRUE)) {
      print(plot_one_celltype(ct))
    }
  }
  
  dev.off()
  
  invisible(list(
    meta_clean = meta_clean,
    stacked_bar = plot_df,
    paired_stats = stats_df,
    paired_sample_proportions = prop_wide
  ))
}


