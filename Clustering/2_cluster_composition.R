#!/usr/bin/env Rscript

# ==============================================================================
# Baseline and activated EBV TCR composition figures
# ==============================================================================
#
# Purpose:
#   Generate publication-ready composition analyses from the final baseline and
#   activated EBV TCR Seurat objects.
#
#   This script reproduces the following analyses:
#     1. Baseline EBV vs non-EBV cluster composition, excluding RPQG cells.
#     2. Baseline paired donor EBV vs non-EBV cluster proportions by cohort.
#     3. Baseline EBV latency class x cluster composition, excluding RPQG cells.
#     4. Activated baseline-latent vs stimulated-latent cluster composition.
#     5. Activated paired donor baseline-latent vs stimulated-latent proportions.
#     6. Activated tetramer x matched-cluster distribution heatmaps.
#     7. Activated stimulated-minus-baseline tetramer redistribution heatmaps.
# #
# Inputs:
#   - Baseline final RDS generated by split integration/clustering:
#       5_baseline_EBV_cluster_reannotated.rds
#   - Activated final RDS generated by split integration/clustering:
#       3_activated_EBV_reannotated.rds
#
# Notes:
#   - Paths are intentionally generic for GitHub.
#   - Update only the config block before running.
#   - The activated object is expected to contain baseline GEMEBV cells and
#     stimulated EBVLCL cells.
#   - If matched_cluster/new_cluster/celltype annotations are missing from the
#     activated object, they are reconstructed from harmony_clusters.
#
# ==============================================================================


