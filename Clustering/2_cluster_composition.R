run_baseline_latency_cluster_composition <- function(obj, cfg) {
  message("\nRunning baseline latency x cluster composition analysis")
  
  require_metadata_columns(
    obj,
    c(
      cfg$tetramer_col,
      cfg$virus_col,
      cfg$latency_col,
      cfg$baseline_celltype_col
    ),
    object_name = "baseline object"
  )
  
  outdir <- cfg$baseline_output_dir
  create_dir(outdir)
  
  meta_clean <- obj@meta.data %>%
    as.data.frame() %>%
    rownames_to_column("cell_id") %>%
    mutate(
      tetramer_clean = str_to_upper(str_trim(as.character(.data[[cfg$tetramer_col]]))),
      virus = clean_virus(.data[[cfg$virus_col]]),
      latency_raw = str_squish(as.character(.data[[cfg$latency_col]])),
      latency_class = case_when(
        latency_raw == "Lytic" ~ "Lytic",
        latency_raw == "Latency I/0" ~ "Latency I/0",
        latency_raw == "Latency II" ~ "Latency II",
        latency_raw == "Latency III" ~ "Latency III",
        TRUE ~ NA_character_
      ),
      latency_class = factor(latency_class, levels = latency_test_order),
      celltype_new = factor(.data[[cfg$baseline_celltype_col]], levels = celltype_order)
    ) %>%
    filter(
      !str_detect(coalesce(tetramer_clean, ""), cfg$exclude_tetramer_regex),
      virus == "EBV",
      !is.na(latency_class),
      !is.na(celltype_new)
    )
  
  rpqg_check <- meta_clean %>%
    summarise(
      remaining_excluded_tetramer_cells = sum(
        str_detect(coalesce(tetramer_clean, ""), cfg$exclude_tetramer_regex)
      ),
      total_cells_after_exclusion = n()
    )
  
  latency_counts <- meta_clean %>%
    count(latency_class, name = "n") %>%
    arrange(latency_class)
  
  cluster_counts <- meta_clean %>%
    count(celltype_new, name = "n") %>%
    arrange(celltype_new)
  
  latency_cluster_counts <- meta_clean %>%
    count(latency_class, celltype_new, name = "n") %>%
    arrange(latency_class, celltype_new)
  
  write_csv(rpqg_check, file.path(outdir, "latency_cluster_RPQG_exclusion_check.csv"))
  write_csv(latency_counts, file.path(outdir, "latency_class_counts_excluding_RPQG.csv"))
  write_csv(cluster_counts, file.path(outdir, "cluster_counts_excluding_RPQG.csv"))
  write_csv(latency_cluster_counts, file.path(outdir, "latency_cluster_observed_counts_excluding_RPQG.csv"))
  
  latency_cluster_table <- table(meta_clean$latency_class, meta_clean$celltype_new)
  
  chisq_check <- suppressWarnings(chisq.test(latency_cluster_table))
  
  if (any(chisq_check$expected < 5)) {
    chisq_res <- chisq.test(
      latency_cluster_table,
      simulate.p.value = TRUE,
      B = 10000
    )
    chisq_method <- "Chi-square test with Monte Carlo simulation"
  } else {
    chisq_res <- chisq_check
    chisq_method <- "Pearson chi-square test"
  }
  
  chisq_df <- (nrow(latency_cluster_table) - 1) * (ncol(latency_cluster_table) - 1)
  chisq_stat <- unname(chisq_res$statistic)
  chisq_p <- chisq_res$p.value
  
  chisq_label <- paste0(
    chisq_method,
    ": chi-square = ",
    round(chisq_stat, 2),
    ", df = ",
    chisq_df,
    ", P = ",
    format.pval(chisq_p, digits = 3, eps = 0.001)
  )
  
  sink(file.path(outdir, "latency_cluster_chisq_result_excluding_RPQG.txt"))
  cat("Association between EBV antigen latency class and transcriptional cluster\n")
  cat("RPQG cells excluded\n\n")
  cat(chisq_label, "\n\n")
  cat("Observed counts:\n")
  print(latency_cluster_table)
  cat("\nExpected counts:\n")
  print(chisq_res$expected)
  cat("\nStandardised residuals:\n")
  print(chisq_res$stdres)
