main <- function(cfg = config) {
  create_dir(cfg$baseline_output_dir)
  create_dir(cfg$activated_output_dir)
  
  baseline_obj <- load_seurat(cfg$baseline_rds)
  activated_obj <- load_seurat(cfg$activated_rds)
  
  activated_obj <- annotate_activated_clusters_if_needed(activated_obj, cfg)
  activated_obj <- add_stim_status(activated_obj, cfg)
  
  baseline_ebv_results <- run_baseline_ebv_vs_negative(baseline_obj, cfg)
  baseline_latency_results <- run_baseline_latency_cluster_composition(baseline_obj, cfg)
  
  activated_composition_results <- run_activated_baseline_vs_stimulated(activated_obj, cfg)
  
  # Use the activated object returned from the composition function because it
  # contains stim_status and any reconstructed annotations.
  activated_obj <- activated_composition_results$activated_object
  
  activated_tcr <- export_activated_tcr_metadata(activated_obj, cfg)
  
  tetramer_distribution_results <- run_tetramer_distribution_heatmap(activated_tcr, cfg)
  tetramer_delta_results <- run_tetramer_delta_heatmap(activated_tcr, cfg)
  tetramer_delta_split_results <- run_tetramer_delta_heatmap_by_cohort(activated_tcr, cfg)
  writeLines(
    c(
      "Baseline and activated EBV TCR composition script complete.",
      "",
      "Configuration:",
      capture.output(str(cfg)),
      "",
      "Session information:",
      capture.output(sessionInfo())
    ),
    file.path(cfg$activated_output_dir, "sessionInfo_baseline_activated_composition_figures.txt")
  )
  
  invisible(list(
    baseline_ebv = baseline_ebv_results,
    baseline_latency = baseline_latency_results,
    activated_composition = activated_composition_results,
    activated_tcr = activated_tcr,
    tetramer_distribution = tetramer_distribution_results,
    tetramer_delta = tetramer_delta_results,
    tetramer_delta_split = tetramer_delta_split_results
  ))
}

main(config)
