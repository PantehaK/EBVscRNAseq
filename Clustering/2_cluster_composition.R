        labels = percent_format(accuracy = 1),
        expand = expansion(mult = c(0, 0.02))
      ) +
      labs(
        title = paste0("Cluster ", cl),
        x = NULL,
        y = "Proportion of latent T cells"
      ) +
      theme_classic(base_size = 14) +
      theme(
        plot.title = element_text(face = "bold", size = 14, hjust = 0.5),
        strip.text = element_blank(),
        strip.background = element_blank(),
        axis.text.x = element_text(size = 12, colour = "black", angle = 30, hjust = 1),
        axis.text.y = element_text(size = 14, colour = "black"),
        axis.title.y = element_text(size = 15, colour = "black"),
        legend.position = "none"
      )
  }
  
  pdf(
    file.path(outdir, "paired_scatter_baseline_vs_stimulated_latent_cluster_proportions_by_cohort_one_page_per_cluster.pdf"),
    width = 7,
    height = 5
  )
  
  for (cl in cluster_order) {
    if (any(scatter_df$new_cluster == cl, na.rm = TRUE)) {
