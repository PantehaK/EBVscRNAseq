      p_label = paste0("FDR=", format_p(FDR)),
      significance = case_when(
        is.na(FDR) ~ "NA",
        FDR < 0.001 ~ "***",
        FDR < 0.01 ~ "**",
        FDR < 0.05 ~ "*",
        TRUE ~ "ns"
      ),
      new_cluster = factor(new_cluster, levels = cluster_order)
    ) %>%
    arrange(cohort, new_cluster)
  
