      bg = bg
    )
  }
}

run_paired_wilcox <- function(x, y, min_n = 3) {
  ok <- !is.na(x) & !is.na(y)
  
  if (sum(ok) < min_n) {
    return(NA_real_)
  }
  
  if (all((x[ok] - y[ok]) == 0)) {
    return(1)
  }
  
  wilcox.test(
    x[ok],
    y[ok],
    paired = TRUE,
    exact = FALSE
  )$p.value
}

annotate_activated_clusters_if_needed <- function(obj, cfg) {
  harmony_to_matched_cluster <- c(
    "0" = "0",
    "1" = "2",
    "2" = "1",
    "3" = "7",
    "4" = "3",
    "5" = "5",
    "6" = "6",
    "7" = "4",
    "8" = "8"
  )
  
  matched_to_new_cluster <- c(
    "2" = "0",
    "0" = "1",
    "8" = "8",
    "1" = "2",
    "3" = "3",
    "4" = "4",
    "6" = "5",
    "7" = "7",
    "5" = "6"
  )
  
  matched_to_celltype <- c(
    "0" = "Naive/early TCM",
    "1" = "TEM",
    "8" = "TEM",
    "7" = "T activated EM",
    "4" = "T activated CM",
    "6" = "T activated EM",
    "2" = "T Late EM",
    "3" = "CTL",
    "5" = "T innate-like"
  )
  
  if (!cfg$activated_matched_cluster_col %in% colnames(obj@meta.data)) {
    require_metadata_columns(obj, "harmony_clusters", object_name = "activated object")
    obj@meta.data[[cfg$activated_matched_cluster_col]] <- unname(
      harmony_to_matched_cluster[as.character(obj@meta.data$harmony_clusters)]
    )
  }
  
  if (!cfg$activated_cluster_col %in% colnames(obj@meta.data)) {
    matched <- as.character(obj@meta.data[[cfg$activated_matched_cluster_col]])
    obj@meta.data[[cfg$activated_cluster_col]] <- unname(matched_to_new_cluster[matched])
  }
  
  if (!"celltype" %in% colnames(obj@meta.data)) {
    matched <- as.character(obj@meta.data[[cfg$activated_matched_cluster_col]])
    obj@meta.data$celltype <- unname(matched_to_celltype[matched])
  }
  
  obj@meta.data[[cfg$activated_cluster_col]] <- factor(
    as.character(obj@meta.data[[cfg$activated_cluster_col]]),
    levels = cluster_order
  )
  
  obj@meta.data[[cfg$activated_matched_cluster_col]] <- factor(
    as.character(obj@meta.data[[cfg$activated_matched_cluster_col]]),
    levels = matched_cluster_order
  )
  
  obj
}


