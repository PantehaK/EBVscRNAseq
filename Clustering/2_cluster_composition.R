      "Baseline_latent" = "Baseline latent",
      "Stimulated_latent" = "Stimulated latent"
    )) +
    scale_y_continuous(labels = percent_format(accuracy = 1), expand = c(0, 0)) +
    labs(
      x = NULL,
      y = "Proportion of latent T cells",
      fill = "Cluster"
    ) +
    theme_classic(base_size = 14) +
    theme(
      axis.text.x = element_text(size = 14, colour = "black"),
      axis.text.y = element_text(size = 14, colour = "black"),
      axis.title.y = element_text(size = 16, colour = "black"),
      legend.title = element_text(size = 12),
      legend.text = element_text(size = 11)
    )
  
  save_plot_with_pdf(
    p_bar,
    file.path(outdir, "stacked_barplot_baseline_vs_stimulated_latent"),
    width = 7,
    height = 7,
    dpi = cfg$png_dpi,
    bg = "transparent",
    save_pdf = cfg$save_pdf
  )
  
  write_csv(plot_df, file.path(outdir, "stacked_barplot_baseline_vs_stimulated_latent.csv"))
  
  meta_df <- obj@meta.data %>%
    as.data.frame() %>%
    rownames_to_column("cell") %>%
    mutate(
      patient_id = as.character(.data[[cfg$sample_col]]),
      cohort = clean_cohort(.data[[cfg$cohort_col]], ms_label = "MS"),
      cohort = factor(cohort, levels = c("Control", "MS")),
      new_cluster = factor(as.character(.data[[cfg$activated_cluster_col]]), levels = cluster_order)
    ) %>%
    filter(
      stim_status %in% c("Baseline_latent", "Stimulated_latent"),
      cohort %in% c("Control", "MS"),
      !is.na(new_cluster),
      !is.na(patient_id)
    )
  
