run_activated_baseline_vs_stimulated <- function(obj, cfg) {
  message("\nRunning activated baseline-latent vs stimulated-latent composition analysis")
  
  require_metadata_columns(
    obj,
    c(
      cfg$sample_col,
      cfg$cohort_col,
      cfg$batch_col,
      cfg$lifecycle_col,
      cfg$activated_cluster_col
    ),
    object_name = "activated object"
  )
  
  outdir <- cfg$activated_output_dir
  create_dir(outdir)
  
