run_tetramer_delta_heatmap <- function(activated_tcr, cfg) {
  message("\nRunning activated tetramer redistribution delta heatmap")
  
  outdir <- cfg$activated_output_dir
  create_dir(outdir)
  
  prop_df <- prepare_tetramer_proportions(
    activated_tcr = activated_tcr,
    cfg = cfg,
    split_by_cohort = FALSE
  )
  
  delta_df <- prop_df %>%
    select(tetramer, condition, matched_cluster, n, prop, prop_percent, total_tetramer_cells) %>%
    pivot_wider(
      names_from = condition,
      values_from = c(n, prop, prop_percent, total_tetramer_cells),
      values_fill = 0
    ) %>%
    mutate(
      delta_prop = prop_Stimulated - prop_Baseline,
      delta_percent = delta_prop * 100
    )
  
  global_stats <- prop_df %>%
    select(tetramer, condition, matched_cluster, n) %>%
    group_by(tetramer) %>%
    summarise(
      global_p_value = {
        tab <- xtabs(n ~ condition + matched_cluster)
        tab <- tab[, colSums(tab) > 0, drop = FALSE]
        
        if (nrow(tab) < 2 || ncol(tab) < 2 || any(rowSums(tab) == 0)) {
          NA_real_
        } else {
          suppressWarnings(chisq.test(tab)$p.value)
        }
      },
      .groups = "drop"
    ) %>%
    mutate(
      global_FDR = p.adjust(global_p_value, method = "BH"),
      global_Holm = p.adjust(global_p_value, method = "holm")
    )
  
  stats_df <- delta_df %>%
    rowwise() %>%
    mutate(
      baseline_in_cluster = n_Baseline,
      baseline_out_cluster = total_tetramer_cells_Baseline - n_Baseline,
      stimulated_in_cluster = n_Stimulated,
      stimulated_out_cluster = total_tetramer_cells_Stimulated - n_Stimulated,
      p_value = if (
        total_tetramer_cells_Baseline == 0 ||
        total_tetramer_cells_Stimulated == 0
      ) {
        NA_real_
      } else {
        fisher.test(
          matrix(
            c(
              baseline_in_cluster,
              baseline_out_cluster,
              stimulated_in_cluster,
              stimulated_out_cluster
            ),
            nrow = 2,
            byrow = TRUE
          )
        )$p.value
      }
    ) %>%
    ungroup() %>%
    mutate(
      FDR = p.adjust(p_value, method = "BH"),
      Holm = p.adjust(p_value, method = "holm"),
      Bonferroni = p.adjust(p_value, method = "bonferroni")
    ) %>%
    left_join(global_stats, by = "tetramer") %>%
    mutate(
      sig = case_when(
        global_Holm < cfg$global_alpha &
          Holm < 0.001 &
          abs(delta_percent) >= cfg$min_delta_percent ~ "***",
        global_Holm < cfg$global_alpha &
          Holm < 0.01 &
          abs(delta_percent) >= cfg$min_delta_percent ~ "**",
        global_Holm < cfg$global_alpha &
          Holm < cfg$tile_alpha &
          abs(delta_percent) >= cfg$min_delta_percent ~ "*",
        TRUE ~ ""
      )
    )
  
  plot_df <- stats_df %>%
    mutate(
      tetramer = factor(tetramer, levels = rev(tetramer_order)),
      matched_cluster = factor(matched_cluster, levels = matched_cluster_order)
    )
  
  max_abs <- max(abs(plot_df$delta_percent), na.rm = TRUE)
  
  if (is.na(max_abs) || max_abs == 0) {
    max_abs <- 1
  }
  
  p_delta <- ggplot(
    plot_df,
    aes(x = matched_cluster, y = tetramer, fill = delta_percent)
  ) +
    geom_tile(color = "grey90", linewidth = 0.25) +
    geom_text(aes(label = sig), size = 4.5, fontface = "bold") +
    scale_fill_gradientn(
      colours = custom_cols,
      limits = c(-max_abs, max_abs),
      oob = scales::squish,
      breaks = pretty(c(-max_abs, max_abs), n = 7),
      name = "Change after\nstimulation\n(% points)"
    ) +
    labs(
      x = "Matched cluster",
      y = "Tetramer",
      title = "Redistribution of EBV-specific tetramer+ cells after stimulation",
      subtitle = paste0(
        "Stimulated minus baseline proportion within each tetramer; ",
        "stars require global Holm < 0.05, tile Holm < 0.05, and >=",
        cfg$min_delta_percent,
        " percentage-point shift"
      )
    ) +
