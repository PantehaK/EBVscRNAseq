prepare_tetramer_proportions <- function(activated_tcr, cfg, split_by_cohort = FALSE) {
  required_cols <- c(cfg$batch_col, cfg$tetramer_col, cfg$activated_matched_cluster_col)
  
  if (isTRUE(split_by_cohort)) {
    required_cols <- c(required_cols, cfg$cohort_col)
  }
  
  missing_cols <- setdiff(required_cols, colnames(activated_tcr))
  
  if (length(missing_cols) > 0) {
    stop("Missing required columns in activated metadata: ", paste(missing_cols, collapse = ", "))
  }
  
  if (isTRUE(split_by_cohort)) {
    prop_df <- activated_tcr %>%
      mutate(
        condition = clean_condition_from_batch(.data[[cfg$batch_col]]),
        cohort = clean_cohort(.data[[cfg$cohort_col]], ms_label = "MS"),
        tetramer = as.character(.data[[cfg$tetramer_col]]),
        matched_cluster = as.character(.data[[cfg$activated_matched_cluster_col]])
      ) %>%
      filter(
        !is.na(condition),
        cohort %in% c("Control", "MS"),
        tetramer %in% tetramer_order,
        matched_cluster %in% matched_cluster_order
      ) %>%
      count(cohort, tetramer, condition, matched_cluster, name = "n") %>%
      complete(
        cohort = c("Control", "MS"),
        tetramer = tetramer_order,
        condition = c("Baseline", "Stimulated"),
        matched_cluster = matched_cluster_order,
        fill = list(n = 0)
      ) %>%
      group_by(cohort, tetramer, condition) %>%
      mutate(
        total_tetramer_cells = sum(n),
        prop = if_else(total_tetramer_cells > 0, n / total_tetramer_cells, 0),
        prop_percent = prop * 100
      ) %>%
      ungroup()
  } else {
    prop_df <- activated_tcr %>%
      mutate(
        condition = clean_condition_from_batch(.data[[cfg$batch_col]]),
        tetramer = as.character(.data[[cfg$tetramer_col]]),
        matched_cluster = as.character(.data[[cfg$activated_matched_cluster_col]])
      ) %>%
      filter(
        !is.na(condition),
        tetramer %in% tetramer_order,
        matched_cluster %in% matched_cluster_order
      ) %>%
      count(tetramer, condition, matched_cluster, name = "n") %>%
      complete(
        tetramer = tetramer_order,
        condition = c("Baseline", "Stimulated"),
        matched_cluster = matched_cluster_order,
        fill = list(n = 0)
      ) %>%
      group_by(tetramer, condition) %>%
      mutate(
        total_tetramer_cells = sum(n),
        prop = if_else(total_tetramer_cells > 0, n / total_tetramer_cells, 0),
        prop_percent = prop * 100
      ) %>%
      ungroup()
  }
  
  prop_df
}

