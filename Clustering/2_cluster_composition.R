create_dir <- function(path) {
  dir.create(path, recursive = TRUE, showWarnings = FALSE)
}

check_file_exists <- function(path) {
  if (!file.exists(path)) {
    stop("Input file does not exist: ", path)
  }
}

load_seurat <- function(path) {
  check_file_exists(path)
  obj <- readRDS(path)
  
  if (!inherits(obj, "Seurat")) {
    stop("RDS does not contain a Seurat object: ", path)
  }
  
  obj
}

require_metadata_columns <- function(obj, cols, object_name = "object") {
  missing_cols <- setdiff(cols, colnames(obj@meta.data))
  
  if (length(missing_cols) > 0) {
    stop(
      "Missing required metadata columns in ", object_name, ": ",
      paste(missing_cols, collapse = ", ")
    )
  }
  
  invisible(TRUE)
}

format_p <- function(p) {
  case_when(
    is.na(p) ~ "NA",
    p < 0.001 ~ "<0.001",
    TRUE ~ sprintf("%.3f", p)
  )
}

clean_cohort <- function(x, ms_label = "MS") {
  x_clean <- str_to_lower(str_trim(as.character(x)))
  
  case_when(
    x_clean %in% c("ms", "pwms", "multiple sclerosis", "rrms", "ppms", "spms") ~ ms_label,
    x_clean %in% c(
      "control", "controls", "healthy control", "healthy controls",
      "hc", "nms", "non-ms", "non_ms", "non ms"
    ) ~ "Control",
    TRUE ~ as.character(x)
  )
}

clean_virus <- function(x) {
  x_clean <- str_to_lower(str_trim(as.character(x)))
  
  case_when(
    x_clean == "ebv" ~ "EBV",
    x_clean %in% c("negative", "neg", "non-ebv", "non_ebv", "non ebv") ~ "Negative",
    TRUE ~ as.character(x)
  )
}

clean_condition_from_batch <- function(batch) {
  batch_clean <- str_to_upper(str_trim(as.character(batch)))
  
  case_when(
    str_detect(batch_clean, "^GEMEBV") ~ "Baseline",
    str_detect(batch_clean, "^EBVLCL") ~ "Stimulated",
    TRUE ~ NA_character_
  )
}

add_stim_status <- function(obj, cfg) {
  require_metadata_columns(
    obj,
    c(cfg$batch_col, cfg$lifecycle_col),
    object_name = "activated object"
  )
  
  obj@meta.data <- obj@meta.data %>%
    mutate(
      batch_clean = str_to_upper(str_trim(as.character(.data[[cfg$batch_col]]))),
      lifecycle_clean = str_to_lower(str_trim(as.character(.data[[cfg$lifecycle_col]]))),
      stim_status = case_when(
        str_detect(batch_clean, "^GEMEBV") & lifecycle_clean == "latent" ~ "Baseline_latent",
        str_detect(batch_clean, "^EBVLCL") & lifecycle_clean == "latent" ~ "Stimulated_latent",
        TRUE ~ "Other"
      ),
      stim_status = factor(
        stim_status,
        levels = c("Baseline_latent", "Stimulated_latent", "Other")
      )
    )
  
  obj$stim_status <- obj@meta.data$stim_status
  obj
}

