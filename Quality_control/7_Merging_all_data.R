write.csv(
  object_summary_df,
  config$object_summary_csv,
  row.names = FALSE
)

write.csv(
  metadata_join$join_summary,
  config$metadata_join_summary_csv,
  row.names = FALSE
)

write.csv(
  metadata_join$missing_metadata,
  config$missing_sample_metadata_csv,
  row.names = FALSE
)

writeLines(
  c(
    "Configuration:",
    capture.output(str(config)),
    "",
    "Datasets:",
    capture.output(str(datasets)),
    "",
    "Session information:",
    capture.output(sessionInfo())
  ),
  config$session_info_file
)

message("\nSaved final merged object to: ", config$output_rds)
message("Saved object summary to: ", config$object_summary_csv)
message("Saved metadata join summary to: ", config$metadata_join_summary_csv)
message("Saved missing metadata IDs to: ", config$missing_sample_metadata_csv)
message("Saved session info to: ", config$session_info_file)

