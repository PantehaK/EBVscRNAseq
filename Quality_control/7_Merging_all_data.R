processed <- lapply(
  datasets,
  process_one_object,
  cfg = config
)

obj_list <- lapply(processed, `[[`, "obj")
names(obj_list) <- vapply(datasets, `[[`, character(1), "dataset_id")

object_summary_df <- bind_rows(lapply(processed, `[[`, "summary"))

# Harmonise ADT assays across all objects after all objects are loaded.
obj_list <- harmonise_adt_features_across_objects(
  obj_list = obj_list,
  cfg = config
)

# Rebuild the summary after ADT harmonisation.
object_summary_df_after_adt <- bind_rows(
  Map(
    function(obj, dataset_cfg) {
      make_object_summary(
        obj = obj,
        dataset_id = dataset_cfg$dataset_id,
        input_rds = dataset_cfg$input_rds,
        cfg = config
      ) %>%
        mutate(summary_stage = "after_adt_harmonisation")
    },
    obj_list,
    datasets
  )
)

object_summary_df <- object_summary_df %>%
  mutate(summary_stage = "before_adt_harmonisation") %>%
  bind_rows(object_summary_df_after_adt)

# Make metadata columns consistent before final merge.
obj_list <- harmonise_metadata_columns(obj_list)

message("\nMerging all processed objects...")
merged_obj <- merge_all_objects(obj_list)

message("Merged object contains ", ncol(merged_obj), " cells.")

# Join sample-level metadata.
message("\nReading and joining sample metadata...")
sample_df <- read_sample_metadata(config)

metadata_join <- join_sample_metadata(
  obj = merged_obj,
  sample_df = sample_df,
  cfg = config
)

