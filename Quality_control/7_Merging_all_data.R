#!/usr/bin/env Rscript

# ==============================================================================
# Merge all processed Seurat objects and add sample metadata
# ==============================================================================
#
# Purpose:
#   This script combines the merged outputs from 6_VDJ_merge.R into one final
#   Seurat object. It:
#     1. loads multiple merged Seurat objects,
#     2. harmonises ADT assay features across objects,
#     3. normalises the harmonised ADT assays,
#     4. preserves or optionally recalculates tetramer metadata,
#     5. removes unwanted metadata columns,
#     6. optionally drops assays that make merging difficult, such as SCT,
#     7. merges all objects into one final Seurat object,
#     8. joins external sample-level metadata, and
#     9. saves summaries, logs and session information.
#
# Expected input:
#   RDS files generated by 6_VDJ_merge.R, usually named:
#     - 6_SCT_merged_all_samples.rds
#     - 6_SCT_merged_all_samples_EBV2.rds
#     - 6_SCT_merged_all_samples_GEM11.rds
#
# Notes:
#   - Tetramer calls are expected to already exist from the ADT/tetramer calling
#     script and should have been carried forward by 6_VDJ_merge.R.
#   - Global PBMC objects are assigned tetramer = "negative" by default.
#   - If you really want to re-call tetramers after ADT harmonisation, set
#     recalculate_tetramer_from_adt = TRUE for the relevant dataset.
#
# ==============================================================================


